
const PI = 3.14

function add(num1, num2) {
    return num1 + num2
}

function subtract(num1, num2) {
    return num1 - num2
}

function multiply(num1, num2) {
    return num1 * num2
}

function divide(num1, num2) {
    return num1 / num2
}

function modulo(num1, num2) {
    return num1 % num2
}

module.exports = {
    PI, add, subtract, multiply, divide, modulo
}