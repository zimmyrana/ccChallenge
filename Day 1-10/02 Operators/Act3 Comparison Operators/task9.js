let num1 = 10;
let num2 = 23;

if (num1 >= num2){
    console.log(`${num1} is greater than ${num2}`)
} else {
    console.log(`${num1} is less than ${num2}`)
}

num2 = 10

if (num1 >= num2){
    console.log(`${num1} is greater than or equal ${num2}`)
} else {
    console.log(`${num1} is less than ${num2}`)
}

